console.log(`Площа кола з радіусом ${radiusCircle} становить: ${areaCircle}`);

// 2. Запитай у користувача відстань в кілометрах між двома містами і за скільки годин він хоче дістатися. Порахуй швидкість, з якою необхідно рухатися, щоб встигнути вчасно
let distance = +prompt('Введіть відстань в кілометрах між двома містами');
let time = +prompt(`Введіть час: за скільки годин ви хоче подолати відстань ${distance}`);
let speed = Math.ceil(distance / time);
console.log(`Швидкість, з якою необхідно рухатися, щоб встигнути вчасно: ${speed} км/год`);

// 3. Реалізуй конвертор валют. Користувач вводить долари, програма переводить їх в євро. Курс валют зберігається в константі.
const RATE = 1.00371;
let dollar = +prompt('Введіть суму в доларах', '1');
let euro = (dollar * RATE).toFixed(2);
console.log(`${dollar} USD еквівалентно ${euro} EUR`);