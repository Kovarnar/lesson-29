// 1. Запитай ім’я користувача та виведи у відповідь “Привіт, *ім’я*”
let userName = prompt('Як тебе звати?');
console.log(`Привіт, ${userName}!`);

// 2. Запитай рік народження користувача, порахуй його/її вік і виведи результат. Поточний рік вкажи в коді як константу
const CARRENT_YEAR = 2022;
let birthYear = +(prompt('Введіть свій рік народження'));
let userAge = CARRENT_YEAR - birthYear;
console.log(`Тобі ${userAge} років`);

// 3. Запитай у користувача довжину сторони квадрату і виведи периметр цього квадрата
let sideSquare = prompt('Введіть довжину сторони квадрата');
let perimeterSquare = (sideSquare * 4).toFixed(3);
console.log(`Периметр квадрата зі стороною ${sideSquare} становить: ${perimeterSquare}`);

